print("Введите координаты точки А")
x0 = int(input("X0: "))
y0 = int(input("Y0: "))

print("Введите координаты точки B")
x1 = int(input("X1: "))
y1 = int(input("Y1: "))

if (x0 ** 2 + y0 ** 2) < (x1 ** 2 + y1 ** 2):
    print("Точка А ближе к началу координат!")
else:
    print("Точка B ближе к началу координат!")